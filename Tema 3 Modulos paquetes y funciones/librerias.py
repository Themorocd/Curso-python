 #Librerias y versiones de python
 
 
# NumPy. Una de las librerías más populares. Utilizada para computación numérica y científica. 
# Pandas. Librería orientada al análisis de datos. 
# Matplotlib. Permite generar todo tipo de gráficos. 
# SciPi. Contiene algoritmos matemáticos. 
# TensorFlow. Esta librería de aprendizaje automático dispone de un API para Python. 
# Requests. Permite realizar peticiones HTTP. 
# Scrapy y Beautiful Soup. Ambas librerías sirven para analizar documentos web y automatizar procesos de obtención de información (conocidos como web scrapping). 
# Kivy. Permite desarrollar aplicaciones en Python para dispositivos móviles. 
# Tkinter, PySide o PyQT. Éstas, sirven para construir interfaces gráficas y, por lo tanto, aplicaciones de escritorio. 
# PyGame. Permite la creación de videojuegos. 
# OpenCV. Una librería veterana dedicada a la visión artificial. 
# gTTS (Google Text-to-Speech). Permite utilizar el API de Google de conversión de texto a voz.


